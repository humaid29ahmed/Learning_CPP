/*
STL - Standard Template Library(It is a predefined library in C++)

STL has four part in it

Iterator - Which are used for iterating the vector or other data structure in STL

Functors - It is usually used for Operator Overloading on the Paranthesis().

Algorithms - This Algorithms part contains predefined Search and Sort Algorithms.

Containers - The contains various Types of Data Structure which helps us to store, manipulate and retrieve data.

With Containers there are various types of Containers

Sequence Container - vector, queue, deques, list, stack

Associative Types - set, multiset, maps, multimaps (binary trees) they contain unique elements

Unordered Types - set, multiset, maps, multimaps (HashMaps) they do not contain unique elements
*/